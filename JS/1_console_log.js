/* 
 * 콘솔 출력을 이용해 자기소개를 출력할 수 있다.
 */

function consoleLog() {
    console.log('1. 콘솔 출력 사용해보기')

    // console.log 써보기
    console.log('[컴퓨터와 대화해보기]');
    console.log('안녕 자바스크립트!!');
    console.log(' ');

    // 자기소개 소과제
    console.log('[자기소개 해보기]');
    console.log('학번: 20504');
    console.log('이름: 홍길동');
    console.log(' ');
}

consoleLog();
